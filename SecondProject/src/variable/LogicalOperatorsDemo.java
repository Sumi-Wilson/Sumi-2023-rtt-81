package variable;

public class LogicalOperatorsDemo {
	public static void main(String[]args) {
		   boolean x = false;
	       boolean y = false;
	       System.out.println("x & y : " + (x & y));
	       System.out.println("x && y : " + (x && y));
	       System.out.println("x | y : " + (x | y));
	       System.out.println("x || y: " + (x || y));
	       System.out.println("x ^ y : " + (x ^ y));
	       System.out.println("!x : " + (!x));

	}

}
